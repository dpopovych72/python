# This is a Python program to demonstrate different data types

# Literal
print("Literal:", 10)

# Integer
positive_reviews = -20
print("Positive reviews :", positive_reviews)

# Float
rating = 0.01
print("Uber Rating :", rating)

# String
name = "Denys"
print("Name :", name)

# Boolean
can_drive = False
print("Knows how to drive :", can_drive)